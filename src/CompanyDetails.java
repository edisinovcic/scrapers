
public class CompanyDetails {

  private String adresa;
  private String kvart;
  private String telefon;
  private String fax;
  private String OIB;
  private String maticniBroj;
  private String webStranica;
  private String djelatnost;
  private String zaposlenih;
  private String radnoVrijeme;
  private String kontaktOsoba;

  CompanyDetails(){

  }

  public String getAdresa() {
    return adresa;
  }

  public void setAdresa(String adresa) {
    this.adresa = adresa;
  }

  public String getKvart() {
    return kvart;
  }

  public void setKvart(String kvart) {
    this.kvart = kvart;
  }

  public String getTelefon() {
    return telefon;
  }

  public void setTelefon(String telefon) {
    this.telefon = telefon;
  }

  public String getFax() {
    return fax;
  }

  public void setFax(String fax) {
    this.fax = fax;
  }

  public String getOIB() {
    return OIB;
  }

  public void setOIB(String OIB) {
    this.OIB = OIB;
  }

  public String getMaticniBroj() {
    return maticniBroj;
  }

  public void setMaticniBroj(String maticniBroj) {
    this.maticniBroj = maticniBroj;
  }

  public String getWebStranica() {
    return webStranica;
  }

  public void setWebStranica(String webStranica) {
    this.webStranica = webStranica;
  }

  public String getDjelatnost() {
    return djelatnost;
  }

  public void setDjelatnost(String djelatnost) {
    this.djelatnost = djelatnost;
  }

  public String getZaposlenih() {
    return zaposlenih;
  }

  public void setZaposlenih(String zaposlenih) {
    this.zaposlenih = zaposlenih;
  }

  public String getRadnoVrijeme() {
    return radnoVrijeme;
  }

  public void setRadnoVrijeme(String radnoVrijeme) {
    this.radnoVrijeme = radnoVrijeme;
  }

  public String getKontaktOsoba() {
    return kontaktOsoba;
  }

  public void setKontaktOsoba(String kontaktOsoba) {
    this.kontaktOsoba = kontaktOsoba;
  }
}
